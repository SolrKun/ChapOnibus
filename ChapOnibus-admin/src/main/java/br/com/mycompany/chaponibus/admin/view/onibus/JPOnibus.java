    public void preencherTabela() {
        DefaultTableModel modelo = (DefaultTableModel) jTOnibus.getModel();
        modelo.setNumRows(0);
        
        try {
            List<Onibus> lista = onibusDAO.listarTodos();
        
            for (Onibus o : lista) {
                String nomeRota = (o.getRotaAtual() != null) ? o.getRotaAtual().getNomeRota() : "Nenhuma";

                modelo.addRow(new Object[] {
                    o.getIdOnibus(),
                    o.getPlaca(),
                    o.getModelo(),
                    o.getCapacidade(),
                    nomeRota
                });
            }
            
            jTOnibus.getColumnModel().getColumn(0).setMinWidth(0);
            jTOnibus.getColumnModel().getColumn(0).setMaxWidth(0);
            jTOnibus.getColumnModel().getColumn(0).setWidth(0);
            
            jBExcluir.setVisible(false);
            jBEditar.setVisible(false);
            jTOnibus.clearSelection();
            this.ultimaLinhaSelecionada = -1;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar tabela: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }
    

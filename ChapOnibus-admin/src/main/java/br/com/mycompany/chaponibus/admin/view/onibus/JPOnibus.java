    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        int linha = jTOnibus.getSelectedRow();
        if (linha != -1) {
            String placa = jTOnibus.getValueAt(linha, 1).toString();
            int id = Integer.parseInt(jTOnibus.getValueAt(linha, 0).toString());
            
            int confirmacao = JOptionPane.showConfirmDialog(this, 
                    "Deseja realmente excluir o ônibus de placa " + placa + "?",
                    "Confirmar Exclusão", JOptionPane.YES_NO_OPTION);
            
            if (confirmacao == JOptionPane.YES_OPTION) {
                try {
                    if (onibusDAO.excluir(id)) {
                        JOptionPane.showMessageDialog(this, "Ônibus excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                        preencherTabela();
                    } else {
                        JOptionPane.showMessageDialog(this, "Não foi possível excluir o ônibus. Ele pode não existir mais ou estar vinculado a uma rota ativa.", "Erro", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir ônibus: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jBExcluirActionPerformed

    private void jTOnibusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTOnibusMouseClicked
        int linha = jTOnibus.getSelectedRow();
        
        if (linha == -1) {
            jBExcluir.setVisible(false);
            jBEditar.setVisible(false);
            ultimaLinhaSelecionada = -1;
            return;
        }
        
        if (evt.getClickCount() == 2) {
            jBEditarActionPerformed(null);
            return;
        }
        
        if (linha == ultimaLinhaSelecionada) {
            jBExcluir.setVisible(false);
            jBEditar.setVisible(false);
            jTOnibus.clearSelection(); 
            ultimaLinhaSelecionada = -1;
        } else {
            jBExcluir.setVisible(true);
            jBEditar.setVisible(true);
            ultimaLinhaSelecionada = linha;
        }
    }//GEN-LAST:event_jTOnibusMouseClicked

    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        int linha = jTOnibus.getSelectedRow();
        
        if (linha != -1) {
            int id = Integer.parseInt(jTOnibus.getValueAt(linha, 0).toString());
            
            Onibus onibus = onibusDAO.procurarOnibus(id);
            
            if (onibus != null) {
                Window janelaMae = SwingUtilities.getWindowAncestor(this);
                Frame framePai = (janelaMae instanceof Frame) ? (Frame) janelaMae : null;

                JDCadastrarOnibus telaEditarOnibus = new JDCadastrarOnibus(framePai, true, onibus);
                telaEditarOnibus.setLocationRelativeTo(janelaMae);
                telaEditarOnibus.setVisible(true);

                if (telaEditarOnibus.isSalvouComSucesso()) {
                    preencherTabela();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Erro: Nenhum ônibus encontrado ou selecionado", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jBEditarActionPerformed



    private void carregarComboBoxRotas() {
        RotaDAO rotaDAO = new RotaDAO();
        try {
            List<Rota> listaDasRotas = rotaDAO.listarTodas(); 
            DefaultComboBoxModel<Rota> model = new DefaultComboBoxModel<>();

            for (Rota r : listaDasRotas) {
                model.addElement(r);
            }
            jCBRota.setModel(model);
            jCBRota.setSelectedIndex(-1);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, 
                "Erro ao carregar as rotas do banco de dados:\n" + ex.getMessage(), 
                "Erro de Conexão", 
                JOptionPane.ERROR_MESSAGE);
        }
    }
    

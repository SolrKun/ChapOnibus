    private void jBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarActionPerformed
        String placa = jTFPlaca.getText().trim();
        String modelo = jTFModelo.getText().trim();
        int capacidade = (int) jSCapacidade.getValue();
        Rota rotaSelecionada = (Rota) jCBRota.getSelectedItem(); 

        if (placa.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite uma placa válida", "Aviso", JOptionPane.WARNING_MESSAGE);
            jTFPlaca.requestFocus();
            return;
        }
        
        if (modelo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um modelo válido", "Aviso", JOptionPane.WARNING_MESSAGE);
            jTFModelo.requestFocus();
            return;
        }
        
        if (capacidade <= 0) {
            JOptionPane.showMessageDialog(this, "Digite uma capacidade válida", "Aviso", JOptionPane.WARNING_MESSAGE);
            jSCapacidade.requestFocus();
            return;
        }
        
        if (onibusEmEdicao == null) {
            Onibus novoOnibus = new Onibus();
            novoOnibus.setPlaca(placa);
            novoOnibus.setModelo(modelo);
            novoOnibus.setCapacidade(capacidade);
            novoOnibus.setRotaAtual(rotaSelecionada);

            if (onibusDAO.salvar(novoOnibus)) {
                JOptionPane.showMessageDialog(this, "Ônibus cadastrado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                this.salvouComSucesso = true;
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar ônibus. Verifique os dados ou o console.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            onibusEmEdicao.setModelo(modelo);
            onibusEmEdicao.setCapacidade(capacidade);
            onibusEmEdicao.setRotaAtual(rotaSelecionada);

            if (onibusDAO.atualizar(onibusEmEdicao)) {
                JOptionPane.showMessageDialog(this, "Ônibus atualizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                this.salvouComSucesso = true;
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar ônibus. Verifique os dados ou o console.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jBCadastrarActionPerformed

